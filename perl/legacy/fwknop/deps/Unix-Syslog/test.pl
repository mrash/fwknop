sub check_defined($$) {
  printf "%-20s", $_[0];
  if (defined eval "$_[0]") {
    print " ok $_[1]\n";
    return 0;
  }
  else {
    print " not ok $_[1]\n";
    return 1;
  }
}

sub check_defined_skip($$) {
  printf "%-20s", $_[0];
  if (defined eval "$_[0]") {
    print " ok $_[1]\n";
    return 0;
  }
  else {
    print " skipped $_[1]\n";
    return 1;
  }
}

